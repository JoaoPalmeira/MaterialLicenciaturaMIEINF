    public void addRowToJTable(){
        
        DefaultTableModel model = (DefaultTableModel) tblQuotas.getModel();
        ArrayList<Quota> list = (ArrayList<Quota>) quotas;
        Object rowData[] = new Object[2];
        for(Quota a : list){
            
            rowData[0] = a.getMes();
            rowData[1] = a.getValor();
            
            model.addRow(rowData);
            
            
            
            
        }
        
        
    }
