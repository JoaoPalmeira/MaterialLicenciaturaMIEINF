    /**
     * Actualiza lista de alunos
     */
    private void refreshList() {
        DefaultListModel<String> dlm = new DefaultListModel();
        for(Aluno a : sgt.listarAlunos()) {
            dlm.addElement("a"+a.getNumero());
        }
        jList1.setModel(dlm);
    }
    
