    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int n = Integer.parseInt(jTextField2.getText());
        String nome = jTextField1.getText();
        String turma = jComboBox1.getSelectedItem().toString();
        Aluno a;
        try {
            if(nome.isEmpty()) {
                a = sgt.novoRegisto(n, turma);    
            } else {
                a = sgt.novoRegisto(nome, turma);
            }
            
            sgt.confirma(sgt.getSessao(), a, turma);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao inserir aluno: " + e.getLocalizedMessage());
        }
        refreshList();
    }//GEN-LAST:event_jButton2ActionPerformed

